#!/usr/bin/env bash
# ag-scaffold-brain.sh
# Scaffolds the Antigravity AI global environment structure and compiles templates.

set -e

# 1. Environment Variable Check
if [ ! -f .env ]; then
  echo "=> No .env file found!"
  if [ -f .env.example ]; then
    echo "=> Generating .env from .env.example..."
    cp .env.example .env
    echo "=> Please edit the newly created .env file with your specific OS and Path details, then re-run this script!"
  else
    echo "=> Error: Neither .env nor .env.example found. Please create a .env file."
  fi
  exit 1
fi

echo "=> Sourcing .env configuration..."
set -a
source .env
set +a

# Require core variables
: "${AG_WORKSPACE:?Variable AG_WORKSPACE is not set in .env}"
: "${AG_SETUP_DIR:?Variable AG_SETUP_DIR is not set in .env}"

echo "=> Compiling Markdown Templates..."
mkdir -p compiled
# Expand variables in the setup documentation
for file in requirements.md implementation_plan.md system_instructions.md; do
  if [ -f "$file" ]; then
    # Use envsubst to replace variables and output to the compiled directory
    envsubst < "$file" > "compiled/$file"
    echo "   -> Compiled to compiled/$file"
  fi
done

echo "=> Scaffolding Antigravity Brain Environments..."

# 2. Setup Docs Repository
mkdir -p "${AG_SETUP_DIR}"
if [ ! -d "${AG_SETUP_DIR}/.git" ]; then
    git -C "${AG_SETUP_DIR}" init
    echo "   -> Initialized git repository in ${AG_SETUP_DIR}"
fi

# 3. Skills Repository Directories
mkdir -p "${AG_WORKSPACE}/skills/"{bin,summaries,templates,patches}
if [ ! -d "${AG_WORKSPACE}/skills/.git" ]; then
    git -C "${AG_WORKSPACE}/skills" init
    echo "   -> Initialized git repository in ${AG_WORKSPACE}/skills"
fi

# 4. Scaffold .gitignore
cat << 'EOF' > "${AG_WORKSPACE}/skills/.gitignore"
# Prevent accidental tracking of binary files or secrets
*.env
*.pem
*.key
.DS_Store
__pycache__/
node_modules/
target/
EOF

# 5. Scaffold memories.md
if [ ! -f "${AG_WORKSPACE}/skills/memories.md" ]; then
cat << 'EOF' > "${AG_WORKSPACE}/skills/memories.md"
# Antigravity Correction Memory Ledger

This file contains explicit behavioral directives learned from user corrections.

## Directives
<!-- Use schema: [#tags] | [Context Trigger] -> [Explicit Directive] -->
- `[#governance #sign-off] | [Drafting CLI Commands] -> NEVER append un-discussed flags, targets, or files to a bash command. The command executed MUST match the explicitly approved plan 1:1. No invisible favors.`
- `[#documentation #readme] | [Initializing a Repository] -> ALWAYS ensure the README.md explicitly describes the context of the project, what it should be used for, and contains a "Quick Start / Installation" guide explaining how to configure (e.g., .env) and use the repository. Never leave it as a simple file index.`
- `[#governance #transparency] | [Executing a Plan] -> Explicitly state every specific system tool you are about to use (e.g., replace_file_content, grep_search, write_to_file). Absolute zero surprises in the execution log.`
EOF
fi

# 5.1 Scaffold README.md for Skills Repository
if [ ! -f "${AG_WORKSPACE}/skills/README.md" ]; then
cat << 'EOF' > "${AG_WORKSPACE}/skills/README.md"
# Antigravity Skills & Memory Repository

## Context
This repository acts as the continuous learning "brain" for the Antigravity AI assistant. It safely tracks learned workflows, scripts, and explicit behavioral corrections across all local projects. It is automatically generated by the `ag-scaffold-brain.sh` framework.

## Quick Start
1. Place executable AI skills (prefixed with `ag-`) in the `bin/` directory.
2. Update the `memories.md` file whenever the AI makes a behavioral mistake.
3. **Security Warning**: Do not commit raw secrets to this repository.
EOF
fi

# 6. Scaffold INDEX.md
if [ ! -f "${AG_WORKSPACE}/skills/INDEX.md" ]; then
cat << 'EOF' > "${AG_WORKSPACE}/skills/INDEX.md"
# Antigravity Skills Index

This file acts as a fast-reference manifest for available skills.

## Available Skills
- `ag-scaffold-brain.sh`: Scaffolds the Antigravity global environment architecture.
EOF
fi

echo "=> Antigravity Brain scaffolded successfully!"
